package day5;

public class Motorbike {
    private int year;
    private String color;
    private String model;

    public Motorbike() {
        this.year = 2017;
        this.color = "black";
        this.model = "Hayabusa";
    }

    public int getYear() {
        return year;
    }

    public String getColor() {
        return color;
    }

    public String getModel() {
        return model;
    }
}
